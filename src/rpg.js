export class Character {
  constructor(name, level, exp, inventory, alignment, job, hp, attack, defense, money) {
    this.name = name
    this.level = level
    this.exp = exp
    this.inventory = inventory
    this.alignment = alignment
    this.job = job
    this.hp = hp
    this.attack = attack
    this.defense = defense
    this.money = money
  }
}